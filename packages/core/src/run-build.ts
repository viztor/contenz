/**
 * Programmatic build: generate content data files.
 * Used by the CLI and by @contenz/core/api.
 */

import fs from "node:fs/promises";
import path from "node:path";
import { globby } from "globby";
import pMap from "p-map";
import pc from "picocolors";
import {
  getContentType,
  getSchemaForType,
  loadCollectionConfig,
  loadProjectConfig,
  loadSchemaModule,
  resolveConfig,
} from "./config.js";
import {
  calculateI18nStats,
  type FlatCollectionData,
  generateFlatCollectionFile,
  generateI18nCollectionFile,
  generateIndexFile,
  generateTypeFromZod,
  type I18nCollectionData,
} from "./generator.js";
import { parseContentFile, parseFileName } from "./parser.js";
import { validateMeta } from "./validator.js";

const BUILD_CONCURRENCY = 4;

export interface BuildResult {
  success: boolean;
  errors: number;
  report: string;
  generated: string[];
}

export interface BuildOptions {
  cwd: string;
  dir?: string;
}

/** Index metadata for one collection, used to generate index.ts */
interface IndexMeta {
  name: string;
  types?: string[];
  hasI18n: boolean;
  metaTypeName?: string;
}

async function generateMultiTypeCollectionFile(
  outputPath: string,
  _collectionName: string,
  typeGroups: Map<string, Map<string, I18nCollectionData | FlatCollectionData>>,
  i18n: boolean,
  locales: string[],
  schemaModule: Record<string, unknown>
): Promise<void> {
  let output = `// Auto-generated by contenz - DO NOT EDIT
// Run: contenz build

`;

  for (const typeName of typeGroups.keys()) {
    if (typeName === "default") continue;
    const pascalName = typeName.charAt(0).toUpperCase() + typeName.slice(1);
    const metaTypeName = `${pascalName}Meta`;
    const schema = schemaModule[`${typeName}Meta`] || schemaModule[typeName];
    if (schema && typeof schema === "object" && "_def" in schema) {
      output += generateTypeFromZod(schema as import("zod").ZodTypeAny, metaTypeName);
      output += "\n\n";
    } else {
      output += `export interface ${metaTypeName} {\n  [key: string]: unknown;\n}\n\n`;
    }
  }

  const defaultTypeName = "default";
  for (const [typeName, itemsMap] of typeGroups) {
    if (typeName === defaultTypeName) continue;
    const pascalName = typeName.charAt(0).toUpperCase() + typeName.slice(1);
    const items = Array.from(itemsMap.values()).sort((a, b) => a.slug.localeCompare(b.slug));

    if (i18n) {
      output += `export interface ${pascalName}Entry extends ${pascalName}Meta {
  slug: string;
  file: string;
}

export interface ${pascalName}Item {
  slug: string;
  locales: {
${locales.map((l) => `    ${l}?: ${pascalName}Entry;`).join("\n")}
  };
}

export const ${typeName}s: Record<string, ${pascalName}Item> = `;
      const dataObj: Record<string, unknown> = {};
      for (const item of items as I18nCollectionData[]) {
        const itemData: Record<string, unknown> = { slug: item.slug, locales: {} };
        for (const [locale, entry] of Object.entries(item.locales)) {
          (itemData.locales as Record<string, unknown>)[locale] = {
            slug: item.slug,
            file: entry.file,
            ...entry.meta,
          };
        }
        dataObj[item.slug] = itemData;
      }
      output += JSON.stringify(dataObj, null, 2);
      output += ";\n\n";
      output += `export const ${typeName}sSlugs = Object.keys(${typeName}s) as (keyof typeof ${typeName}s)[];\n\n`;
      const stats = calculateI18nStats(items as I18nCollectionData[]);
      output += `export const ${typeName}sStats = ${JSON.stringify(stats, null, 2)};\n\n`;
    } else {
      output += `export interface ${pascalName}Entry extends ${pascalName}Meta {
  slug: string;
  file: string;
}

export const ${typeName}s: Record<string, ${pascalName}Entry> = `;
      const dataObj: Record<string, unknown> = {};
      for (const item of items as FlatCollectionData[]) {
        dataObj[item.slug] = { slug: item.slug, file: item.file, ...item.meta };
      }
      output += JSON.stringify(dataObj, null, 2);
      output += ";\n\n";
      output += `export const ${typeName}sSlugs = Object.keys(${typeName}s) as (keyof typeof ${typeName}s)[];\n\n`;
      output += `export const ${typeName}sStats = { total: ${items.length} };\n\n`;
    }
  }

  await fs.writeFile(outputPath, output, "utf-8");
}

async function processOneCollection(
  schemaFile: string,
  contentDir: string,
  outputDir: string,
  projectConfig: import("./types.js").ProjectConfig
): Promise<
  | { ok: true; indexMeta: IndexMeta; outputName: string; lines: string[] }
  | { ok: false; lines: string[] }
> {
  const lines: string[] = [];
  const collectionName = path.dirname(schemaFile);
  const collectionPath = path.join(contentDir, collectionName);

  const collectionConfig = await loadCollectionConfig(collectionPath);
  const projectConfigTyped = projectConfig;
  const config = resolveConfig(projectConfigTyped, undefined, collectionConfig);

  const schemaModule = await loadSchemaModule(collectionPath);
  if (!schemaModule) {
    lines.push(pc.red(`${collectionName}/: Failed to load schema.ts`));
    return { ok: false, lines };
  }

  const rawSchema = schemaModule.meta || (schemaModule as Record<string, unknown>).metaSchema;
  if (!rawSchema) {
    lines.push(pc.red(`${collectionName}/: No meta or metaSchema export found`));
    return { ok: false, lines };
  }
  const defaultSchema = rawSchema as import("zod").ZodSchema;

  lines.push(pc.cyan(`${collectionName}/`));

  const extensionPattern = config.extensions.map((e) => `*.${e}`).join(",");
  const contentFiles = await globby(`{${extensionPattern}}`, {
    cwd: collectionPath,
    onlyFiles: true,
    ignore: config.ignore,
  });

  const typeGroups = new Map<string, Map<string, I18nCollectionData | FlatCollectionData>>();
  const defaultTypeName = "default";
  let parseErrors = 0;
  const detectedLocales = new Set<string>();

  for (const file of contentFiles) {
    const filePath = path.join(collectionPath, file);
    const parsed = parseFileName(file, config.i18n, config.slugPattern);
    if (!parsed) {
      lines.push(pc.yellow(`  ⚠ Skipping ${file} (invalid naming pattern)`));
      continue;
    }
    try {
      const result = await parseContentFile(filePath, config);
      const contentType = getContentType(file, config) ?? defaultTypeName;
      const schema =
        contentType !== defaultTypeName
          ? (getSchemaForType(schemaModule, contentType) ?? defaultSchema)
          : defaultSchema;
      const validation = validateMeta(result.meta, schema, file);
      if (!validation.valid) {
        parseErrors++;
        for (const err of validation.errors) {
          lines.push(pc.red(`  ✗ ${file}: ${err.field} - ${err.message}`));
        }
        continue;
      }
      if (!typeGroups.has(contentType)) typeGroups.set(contentType, new Map());
      const itemsMap = typeGroups.get(contentType);
      if (!itemsMap) {
        parseErrors++;
        lines.push(pc.red(`  ✗ ${file}: internal error creating collection type group`));
        continue;
      }
      if (config.i18n && parsed.locale) {
        detectedLocales.add(parsed.locale);
        if (!itemsMap.has(parsed.slug)) {
          itemsMap.set(parsed.slug, { slug: parsed.slug, locales: {} });
        }
        const item = itemsMap.get(parsed.slug) as I18nCollectionData;
        item.locales[parsed.locale] = { file, meta: result.meta };
      } else {
        itemsMap.set(parsed.slug, {
          slug: parsed.slug,
          file,
          meta: result.meta,
        } as FlatCollectionData);
      }
    } catch (error) {
      parseErrors++;
      lines.push(pc.red(`  ✗ ${file}: ${String(error)}`));
    }
  }

  if (parseErrors > 0) {
    return { ok: false, lines };
  }

  const types = config.types?.map((t) => t.name) ?? [];
  const locales = [...detectedLocales].sort();
  const collectionOutputPath = path.join(outputDir, `${collectionName}.ts`);

  if (types.length > 0) {
    for (const [typeName, itemsMap] of typeGroups) {
      if (typeName === defaultTypeName) continue;
      const items = Array.from(itemsMap.values()).sort((a, b) => a.slug.localeCompare(b.slug));
      lines.push(pc.dim(`  ✓ Parsed ${items.length} ${typeName} items`));
    }
    await generateMultiTypeCollectionFile(
      collectionOutputPath,
      collectionName,
      typeGroups,
      config.i18n,
      locales,
      schemaModule as Record<string, unknown>
    );
    lines.push(pc.green(`  ✓ Generated ${collectionName}.ts`));
    return {
      ok: true,
      indexMeta: { name: collectionName, types, hasI18n: config.i18n },
      outputName: `${collectionName}.ts`,
      lines,
    };
  }

  const itemsMap = typeGroups.get(defaultTypeName) ?? new Map();
  const items = Array.from(itemsMap.values()).sort((a, b) => a.slug.localeCompare(b.slug));
  const metaTypeName =
    ((schemaModule as Record<string, unknown>).metaTypeName as string | undefined) ??
    `${collectionName.charAt(0).toUpperCase() + collectionName.slice(1)}Meta`;

  if (config.i18n) {
    await generateI18nCollectionFile(
      collectionOutputPath,
      collectionName,
      items as I18nCollectionData[],
      metaTypeName,
      locales,
      defaultSchema
    );
  } else {
    await generateFlatCollectionFile(
      collectionOutputPath,
      collectionName,
      items as FlatCollectionData[],
      metaTypeName,
      defaultSchema
    );
  }

  lines.push(pc.dim(`  ✓ Parsed ${contentFiles.length} files (${items.length} slugs)`));
  lines.push(pc.green(`  ✓ Generated ${collectionName}.ts`));

  return {
    ok: true,
    indexMeta: { name: collectionName, hasI18n: config.i18n, metaTypeName },
    outputName: `${collectionName}.ts`,
    lines,
  };
}

export async function runBuild(options: BuildOptions): Promise<BuildResult> {
  const lines: string[] = [];
  const cwd = path.resolve(process.cwd(), options.cwd ?? ".");
  const dir = options.dir ?? "content";

  const projectConfig = await loadProjectConfig(cwd);
  const contentDir = path.resolve(cwd, dir);
  const baseConfig = resolveConfig(projectConfig);
  const outputDir = path.resolve(cwd, baseConfig.outputDir);

  await fs.mkdir(outputDir, { recursive: true });

  lines.push(pc.bold("\nBuilding content collections...\n"));
  lines.push(pc.dim(`Output: ${path.relative(cwd, outputDir)}/\n`));

  const schemaFiles = await globby("*/schema.ts", {
    cwd: contentDir,
    onlyFiles: true,
  });

  if (schemaFiles.length === 0) {
    lines.push(pc.yellow("No schema.ts files found in content directories."));
    return {
      success: true,
      errors: 0,
      report: lines.join("\n"),
      generated: [],
    };
  }

  const results = await pMap(
    schemaFiles,
    (schemaFile) => processOneCollection(schemaFile, contentDir, outputDir, projectConfig),
    { concurrency: BUILD_CONCURRENCY }
  );

  for (const r of results) {
    lines.push(...r.lines);
  }

  const succeeded = results.filter(
    (r): r is { ok: true; indexMeta: IndexMeta; outputName: string; lines: string[] } => r.ok
  );
  const indexMetaList = succeeded.map((r) => r.indexMeta);
  const generated = succeeded.map((r) => r.outputName);

  await generateIndexFile(
    path.join(outputDir, "index.ts"),
    indexMetaList.map((m) => ({
      name: m.name,
      types: m.types,
      hasI18n: m.hasI18n,
      metaTypeName: m.metaTypeName,
    }))
  );
  generated.push("index.ts");

  lines.push(pc.green(`\nGenerated ${path.relative(cwd, outputDir)}/index.ts`));

  const failedCount = results.length - succeeded.length;
  if (failedCount > 0) {
    lines.push(pc.red("\nBuild completed with errors."));
    return {
      success: false,
      errors: failedCount,
      report: lines.join("\n"),
      generated,
    };
  }

  lines.push(pc.green("\nBuild complete."));
  lines.push(pc.dim("Run `contenz lint --coverage` to generate coverage report."));
  return {
    success: true,
    errors: 0,
    report: lines.join("\n"),
    generated,
  };
}
